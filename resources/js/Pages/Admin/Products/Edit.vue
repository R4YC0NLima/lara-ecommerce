<template>
    <AppLayout title="Cadastrar Produtos">
        <div class="container mx-auto px-4 py-8">
            <div class="bg-gray-200 rounded-lg shadow-md overflow-hidden">
                <div class="w-full bg-gray-100 p-4 border-b border-gray-200">
                    <div class="font-bold">
                        <h1>Editar Produto</h1>
                    </div>
                </div>
                
                <div class="p-4">
                    <div class="mt-5 md:mt-0 md:col-span-2">
                        <form @submit.prevent="submit">
                            <div class="shadow overflow-hidden sm:rounded-md">
                                <div class="px-4 py-5 bg-white sm:p-6">
                                    <div class="grid grid-cols-6 gap-6">
                                        <div class="col-span-6 sm:col-span-6 lg:col-span-2">
                                            <label for="name" class="block text-sm font-medium text-gray-700">Nome</label>
                                            <input type="text" name="name" v-model="form.name" id="name" autocomplete="name" class="mt-1 focus:ring-indigo-500 focus:border-indigo-500 block w-full shadow-sm sm:text-sm border-gray-300 rounded-md">
                                        </div>

                                        <div class="col-span-6 sm:col-span-3 lg:col-span-2">
                                            <label for="price" class="block text-sm font-medium text-gray-700">Preço</label>
                                            <input type="text" name="price" v-model="form.price" id="price" autocomplete="price" class="mt-1 focus:ring-indigo-500 focus:border-indigo-500 block w-full shadow-sm sm:text-sm border-gray-300 rounded-md">
                                        </div>

                                        <div class="col-span-6 sm:col-span-3 lg:col-span-2">
                                            <label for="categories" class="block text-sm font-medium text-gray-700">Categoria</label>
                                            <select v-model="form.category_id" id="categories" class="form-select appearance-none block w-full px-3 py-1.5 text-base font-normal text-gray-700 bg-white bg-clip-padding bg-no-repeat border border-solid border-gray-300 rounded transition ease-in-out m-0 focus:text-gray-700 focus:bg-white focus:border-blue-600 focus:outline-none" aria-label="Default select example">
                                                <option selected disabled>Selecione</option>
                                                <option v-for="category in categories" :key="category.id" :value="category.id">{{ category.name }}</option>
                                            </select>
                                        </div>

                                        <div class="col-span-6 sm:col-span-3">
                                            <label for="description" class="block text-sm font-medium text-gray-700">Descrição</label>
                                            <input type="text" v-model="form.description" name="description" id="description" autocomplete="given-name" class="mt-1 focus:ring-indigo-500 focus:border-indigo-500 block w-full shadow-sm sm:text-sm border-gray-300 rounded-md">
                                        </div>

                                    </div>
                                </div>
                                <div class="px-4 py-3 bg-gray-50 text-right sm:px-6">
                                    <button type="submit" class="inline-flex justify-center py-2 px-4 border border-transparent shadow-sm text-sm font-medium rounded-md text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500">Save</button>
                                </div>
                            </div>
                        </form>
                    </div>
                </div>
            </div>
        </div>
    </AppLayout>
</template>

<script>
import AppLayout from '@/Layouts/AppLayout.vue';
export default {
    name: 'ProductCreate',
    components: {
        AppLayout
    },
    data() {
        return {
            form: this.$inertia.form({
                name: this.$props.product.name,
                description: this.$props.product.description,
                price: this.$props.product.price,
                category_id: this.$props.product.category_id,
            })
        }
    },
    props: {
        product: Object,
        categories: Object,
    },
    methods: {
        submit() {
            this.form.put(this.route('admin.products.update', this.$props.product.id), this.form)
        }
    }
}
</script>